package frc.robot.commands.AutoCommands;

import edu.wpi.first.wpilibj2.command.ParallelDeadlineGroup;
import edu.wpi.first.wpilibj2.command.SequentialCommandGroup;
import frc.robot.PathContainer;
import frc.robot.RobotContainer;
import frc.robot.commands.SpinIntake;

public class AutoTest extends SequentialCommandGroup {
    public AutoTest(RobotContainer robot) {

        RobotContainer.m_drive.resetOdometry(PathContainer.T1.getInitialPose());

        addCommands(
            new ParallelDeadlineGroup(PathContainer.T1ram, new SpinIntake()),
            new ParallelDeadlineGroup(PathContainer.T2ram, new SpinUpShooter("Low")),
            new ParallelDeadlineGroup(PathContainer.T3ram, new SpinIntake()),
            new ParallelDeadlineGroup(PathContainer.T4ram, new SpinUpShooter("Low"))
        );
    }
}